
public class LOOPS {
	public static void main(String[] args)
	{
	for(int i=5; i<=10;i++)
		System.out.println(i);
	System.out.println("end of for");
	int count = 5;
	while(count<=10)
	{
		System.out.println(count);
		count++;
	}
	System.out.println("end of while");
	int digit = 5;
	do
	{
		System.out.println(digit);
		digit++;
		
	}
	while(digit<=10);
	System.out.println("end of do-while");
}
}
